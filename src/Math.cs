class Program
{
    static void Main(string[] args)
    {
        // Math -----------------------------------------------------------------------

        // The C# Math class has many methods that allows you to perform mathematical
        // tasks on numbers.

        // Math.Max(x,y) --------------------------------------------------------------

        // The Math.Max(x, y) method can be used to find the highest value of x and y:

        Math.Max(5, 10);        // Returns 10, because 10 is greater than 5.

        // Math.Min(x,y) --------------------------------------------------------------
        
        // The Math.Min(x, y) method can be used to find the lowest value of of x and y:

        Math.Min(5, 10);        // Returns 5, because 5 is smaller than 10.

        // Math.Sqrt(x) ---------------------------------------------------------------

        // The Math.Sqrt(x) method returns the square root of x:

        Math.Sqrt(64);          // Returns 8

        // Math.Abs(x) ----------------------------------------------------------------

        // The Math.Abs(x) method returns the absolute(positive) value of x:

        Math.Abs(-4.7);         // Returns 4.7

        // Math.Round() ---------------------------------------------------------------

        // Math.Round() rounds a number to the nearest whole number:

        Math.Round(9.99);       // Returns 10

    }
}